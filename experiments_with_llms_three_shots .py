# Função para extrair código Python (ignorando a palavra python após ```)
def extract_python_code(text):
    """
    Extrai blocos de código Python de um texto que contenha marcadores ```Python, ```python ou apenas ```.

    Args:
        text (str): Texto contendo possíveis blocos de código Python.

    Returns:
        list: Lista de strings com os códigos Python encontrados.
    """
    # Padrão regex para encontrar blocos de código entre ``` (opcionalmente com python/Python)
    pattern = r'```(?:[pP]ython)?(.*?)```'

    # Encontra todos os blocos que correspondem ao padrão
    code_blocks = re.findall(pattern, text, re.DOTALL)

    # Remove espaços em branco no início e final de cada bloco
    cleaned_blocks = [block.strip() for block in code_blocks]

    return cleaned_blocks

# Função para executar código Python e extrair o último valor numérico
def execute_python_code(code):
    """
    Executa um bloco de código Python e captura o último valor numérico impresso.

    Args:
        code (str): Código Python a ser executado.

    Returns:
        float or str: Último valor numérico encontrado na saída ou "erro" se houver falha.
    """
    # Dicionário para capturar variáveis locais
    local_vars = {}

    try:
        # Executa o código
        exec(code, globals(), local_vars)

        # Procura por qualquer variável numérica no ambiente local
        numeric_values = [v for v in local_vars.values() if isinstance(v, (int, float))]

        if numeric_values:
            return numeric_values[-1]  # Retorna o último valor numérico
        else:
            return "erro (nenhum valor numérico encontrado)"

    except Exception as e:
        return "erro"

